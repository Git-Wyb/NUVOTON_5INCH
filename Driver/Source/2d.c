    sysprintf("screen height = %d\n", GFX_HEIGHT);
    sysprintf("screen bpp = %d\n", GFX_BPP);
    sysprintf("screen size = 0x%x\n", GFX_SIZE);
#endif

    outpw(REG_CLK_HCLKEN, inpw(REG_CLK_HCLKEN) | (1<<28));

